/**
 * Диапазоны брейкпоинтов
 */
export enum Breakpoints {
  // phone s: 0-374
  maxWidthPhoneS = 374,

  // phone l: 375-767
  maxWidthPhoneL = 767,

  // tablet m: 768-1023
  maxWidthTabletM = 1023,

  // desktop s: 1024-1439
  maxWidthDesktopS = 1439,

  // desktop m: 1440-9999
  minWidthDesktopM = 1439,
}
